"""Model loading layer for the dashboard.

Loads the calibrated XGBoost classifier from a bundled pickle file.
The pickle is generated by scripts/build_dashboard_assets.py and committed
to the repo so the deployed dashboard can use it without retraining.
"""

from __future__ import annotations

import pickle
from dataclasses import dataclass
from pathlib import Path

# Path to the bundled model pickle.
_MODEL_PATH = Path(__file__).resolve().parent.parent / "data" / "model.pkl"


@dataclass
class ModelArtifact:
    """Bundle of the trained model + the encoder used during training.

    Both are needed for inference: encoder transforms team/venue strings
    into the same one-hot space the model was trained on.
    """

    classifier: object  # CalibratedClassifierCV with XGBoost inside
    encoder: object  # OneHotEncoder fit on training data
    numeric_features: list[str]
    categorical_features: list[str]
    feature_names: list[str]
    training_holdout_metrics: dict


def load_model() -> ModelArtifact:
    """Load the bundled model artifact.

    Raises FileNotFoundError if the model has not been built. Run
    `python -m scripts.build_dashboard_assets` to generate it.
    """
    if not _MODEL_PATH.exists():
        raise FileNotFoundError(
            f"Model artifact not found at {_MODEL_PATH}. "
            "Run `python -m scripts.build_dashboard_assets` to generate it."
        )
    with open(_MODEL_PATH, "rb") as f:
        return pickle.load(f)
